            <p>Рост тарифов на тепловую энергию и постоянное удорожание ископаемых видов топлива заставил предприятия обратиться к более дешевым источникам получения тепловой энергии, к твердотопливным котлам на опилках и биотопливе. Россия имеет в своем распоряжении практически неисчерпаемые запасы древесины и торфа. Даже при современных темпах лесозаготовки естественный прирост лесов превышает объем вырубки примерно на 20%. Предприятиях специализирующиеся на лесопилении и деревопереработке получают помимо выпускаемой продукции еще и достаточное количество отходов древесины, а вместе с ними заботы и затраты по организации их хранения и вывоза. Оптимальное решение в этой ситуации - котельная на опилках. Производственное объединение «Теплоресурс», помимо опыта котлостроения, обладает достаточным опытом для применения в котлах схем эффективного сжигания такого нестандартного топлива. Автоматические водогрейные котлы работающие на щепе и опилках по конструкции самым эффективным образом отвечает этому требованию, обеспечивая устойчивую работу на топливе с повышенной влажностью.</p>
