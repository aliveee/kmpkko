                <p>
                    Водогрейные котлы на опиле и щепе серии КТУ выпускается в стандартной линейке мощностей от 300 до 2500 кВт, представляют собой модульную конструкцию и состоят из следующих основных узлов:
                </p>
                <ol>
                    <li>Теплообменник</li>
                    <li>Люк обслуживания теплообменника</li>
                    <li>Топочная дверь</li>
                    <li>Топка</li>
                    <li>Колосниковая решетка</li>
                    <li>Механизм подачи топлива</li>
                </ol>
                <p>
                    Топливом для котлов этой серии могут служить высоковлажные древесные отходы, фрезерный торф (относительная влажность до 55%) без предварительного подсушивания, сухие древесные отходы.
                </p>
                <p>
                    При подаче автоматическим методом фракция топлива не должна превышать 50x50x5 мм, при ручной подаче максимальные размеры дров до 1 м.
                </p>
                <p>
                    Из оперативного бункера-дозатора с помощью механизма подачи топлива (шнековый транспортер или гидравлический толкатель) топливо поступает в топку котла. Сжигание топлива происходит на наклонной колосниковой решетке, на которой происходит горение как сыпучей (опилки, стружка, щепа, торф, отходы растениеводства), так и кусковой древесины. При сжигании в топочной камере топливо проходит три этапа (три такта сжигания):
                </p>
                <p>
                    <b>Первый этап</b> – подсушивание топлива. В твердотопливных котельных, установленных на деревоперерабатывающих предприятиях, в большинстве случаев в качестве топлива для котлов используют щепу и опилки с высокой влажностью, полученные после распиловки свежих стволов. На фанерных заводах древесину перед обработкой предварительно пропаривают в технологических бассейнах, в этом случае топливо имеет повышенную влажность. На первом этапе движения топлива в топке котла, опилки и щепа из топливного лотка выдавливается непосредственно в топку, где распределяются по ширине топочного фронта, на наклонных колосниках. Испарение влаги происходит от термического воздействия раскаленной футеровки топки и лучистой энергии горения, отраженной от сводового экрана топки.
                </p>
                <p>
                    <b>Второй этап</b> – первичное окисление и выделение СО. Слоевое горение протекает при относительно невысоких температурах 700-750оС на наклонных и горизонтальных колосниках с принудительным раздельным поддувом.
                </p>
                <p>
                    <b>Третий этап</b> - дожиг СО в факеле топки котла длительного горения. Дожиг происходит непосредственно в топке котла, в зоне подачи воздуха (активатора окисления) при температуре 900-950оС, наиболее оптимальной для протекания реакции окисления углерода. Забор воздуха производится из воздушной прослойки между футеровкой и наружной облицовкой котла. Это способствует снижению теплопотерь и увеличению КПД. На каждом такте горения соразмерно подаче топлива организована дозированная подача окислителя (атмосферного воздуха) несколькими вентиляторами (дутьевыми и дожиговыми). После полного сжигания остаточного углерода зола падает через решетку колосника в зольник, откуда вручную (или механически в зависимости от конструкции) удаляется через прочистной люк непосредственно в режиме работы котла.
                </p>
            </div>

            <div class="uc__block">
