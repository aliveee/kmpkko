                <p>
                    <b>Теплообменник</b> -  это специальная конструкция для передачи тепловой энергии от нагретого теплоносителя более холодному. В конструкции котлов серии КТУ применен теплообменник газотрубного типа. Он представляет собой “бочку” с проходящими через нее дымогарными трубами, пронизывающими его от фронта котла к тыльной стороне с поворотом газов на 180° по двухходовой схеме. Дымовые газы, проходя по трубкам, нагревают теплоноситель, циркулирующий внутри теплообменника и омывающий наружные стенки труб. Скорость прохождения дымовых газов определяется уровнем разрежения в топочной камере и регулируется дымососом.
                </p>
                <p>&nbsp;</p>
                <p>Оптимальная скорость движения теплового агента в теплообменнике регулируется автоматически, и обеспечивается насосной станцией. На входе теплообменника применяется теплоизолирующая обмуровка для защиты обшивки от высокотемпературных газов. Используются антивзрывные клапаны по дымовым газам и клапаны группы безопасности по теплоносителю – горячей воде. Подвод теплоносителя осуществляется через входной, а отвод - через выходной коллектор. На коллекторах установлены датчики температуры теплоносителя. Информация с датчиков поступает на пульт управления, позволяя регулировать подачу топлива в топку, тем самым, удерживая заданные параметры теплоносителя. Корпус теплообменника облицован листовым профилем. Жаротрубная конструкция теплообменника минимизирует необходимость химводоподготовки (ХВП) и позволяет проводить обслуживание теплообменника непосредственно в режиме эксплуатации котла. Подача топлива может осуществляться как автоматическим, так и ручным способом. Топливом для котлов этой серии могут служить высоковлажные древесные отходы, фрезерный торф (относительная влажность до 55%) без предварительного подсушивания, сухие древесные отходы. При подаче автоматическим методом фракция топлива не должна превышать 50х50х5 мм, при ручной подаче максимальные размеры дров до 1 м.</p>
                <p>Котлы на опилках и щепе серии КТУ полностью совместимы с автоматической системой управления, предлагаемой нашим предприятием, что повышает безопасность и бесперебойность работы котла в круглосуточном режиме. Для создания запасов топлива и автоматизации топливоподачи отопительные котлы на щепе этой серии дополнительно комплектуются топливными накопителями различной емкости, в зависимости от мощности котла и поставленного задания. Использование воды в качестве теплоносителя, позволяет применять котлы на опилках для передачи тепловой энергии на значительные расстояния, а так же для использования их в сушильных камерах. Дополнительное преимущество применения воды в качестве теплоносителя - это возможность создать котельную на щепе и опилках, с многоконтурной системой теплоснабжения, и распределить потоки от одной котельной к нескольким независимым зонам потребления посредством «гребенок». Для увеличения срока службы котла на щепе применяется многоконтурная гидравлическая схема с разделенными независимыми контурами, с применением межконтурных теплообменников.</p>
